            0x979350FA709C5EF3,
            0x0350125A92067BCD
        };
            
        #endregion

        protected ulong m_flags;
        protected readonly ulong[] m_state = new ulong[8];

        public SkeinBase(HashLib.HashSize a_hash_size, int a_block_size)
            : base((int)a_hash_size, a_block_size)
        {
            Initialize();
        }

        protected override void Finish()
        {
            m_flags |= 0x8000000000000000;

            TransformBlock(m_buffer.GetBytesZeroPadded(), 0);

            m_processed_bytes = 8;
            m_flags = 0xff00000000000000;

            TransformBlock(m_buffer.GetBytesZeroPadded(), 0);
        }

        protected override byte[] GetResult()
        {
            return Converters.ConvertULongsToBytes(m_state, 0, BlockSize / 8).SubArray(0, HashSize);
        }

        public override void Initialize()
        {
            m_flags = 0x7000000000000000;

            switch (HashSize)
            {
                case 28: Array.Copy(SKEIN_IV_224, 0, m_state, 0, SKEIN_IV_224.Length); break;
                case 32: Array.Copy(SKEIN_IV_256, 0, m_state, 0, SKEIN_IV_256.Length); break;
                case 48: Array.Copy(SKEIN_IV_384, 0, m_state, 0, SKEIN_IV_384.Length); break;
                case 64: Array.Copy(SKEIN_IV_512, 0, m_state, 0, SKEIN_IV_512.Length); break;
            }

            base.Initialize();
        }

        public override void TransformBytes(byte[] a_data, int a_index, int a_length)
        {
            Debug.Assert(a_index >= 0);
            Debug.Assert(a_length >= 0);
            Debug.Assert(a_index + a_length <= a_data.Length);

            if (a_length == 0)
                return;

            if (m_buffer.IsFull)
                TransformBlock(m_buffer.GetBytes(), 0);

            if (a_length + m_buffer.Pos > BlockSize)
            {
                if (m_buffer.Feed(a_data, ref a_index, ref a_length, ref m_processed_bytes))
                    TransformBlock(m_buffer.GetBytes(), 0);
            }

            while (a_length > BlockSize)
            {
                m_processed_bytes += (uint)BlockSize;
                TransformBlock(a_data, a_index);
                a_length -= BlockSize;
                a_index += BlockSize;
            }

            if (a_length > 0)
                m_buffer.Feed(a_data, ref a_index, ref a_length, ref m_processed_bytes);
        }
    };
