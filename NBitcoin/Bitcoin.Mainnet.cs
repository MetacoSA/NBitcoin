			builder.SetBech32(Bech32Type.WITNESS_PUBKEY_ADDRESS, encoder);
			builder.SetBech32(Bech32Type.WITNESS_SCRIPT_ADDRESS, encoder);
			builder.SetBech32(Bech32Type.TAPROOT_ADDRESS, encoder);


